#include <iostream>

using namespace std;

int main()
{
    int misbah = 5;//declaration and initialization
    cout << "value of variable is " << misbah << endl;//initial value of variable
    cout << "value before post-increment is " << misbah << endl;
    misbah++;
    cout << "value after post-increment is " << misbah << endl;
    return 0;
}
